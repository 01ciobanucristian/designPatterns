package solid.ocp;

public class Dreptunghi {
    private final int latime;
    private final int lungime;

    public Dreptunghi(int latime, int lungime) {
        this.latime = latime;
        this.lungime = lungime;
    }

    public int getLatime() {
        return latime;
    }

    public int getLungime() {
        return lungime;
    }
}
